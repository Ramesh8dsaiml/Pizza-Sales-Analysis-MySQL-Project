-- Calculate the total revenue generated from pizza sales.
-- ,max, min.  
SELECT 
    ROUND(MIN(orders_details.quantity * pizzas.price),
            2) AS total_sales
FROM
    orders_details
        JOIN
    pizzas ON pizzas.pizza_id = orders_details.pizza_id;

